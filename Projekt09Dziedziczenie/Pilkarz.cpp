#include "Pilkarz.h"

Pilkarz::Pilkarz(int liczbaMeczy, int liczbaGoli, int wiek, std::string plec, std::string nazwisko): Osoba(wiek, plec, nazwisko) {
	this->liczbaMeczy = liczbaMeczy;
	this->liczbaGoli = liczbaGoli;
}

void Pilkarz::drukuj() {
	Osoba::drukuj();
	std::cout<<"Liczba goli: "<<liczbaGoli
		<<"\nLiczba meczy: "<<liczbaMeczy<<std::endl;
}
